package session02;

public class DataTypes {

	public static void main(String[] args) {
		
		// Primitive Data Types
		int a = 12;
		double b = 12.7;
		char c = 'A'; // Only a single letter
		boolean bool = true;
		
		// Non- primitive Data Type
		String name =  "Java";    // Collection/ Sequence of characters
		// All primitive data types are in red color but non-primitive data type is in different color
		
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(bool);
		System.out.println(name);
	}

}
